public class FlutterBuilder extends FrameworkBuilder {

    @Override
    public void buildFrameworkName() {
        fProduct.frameworkName = "Flutter";
    }

    @Override
    public void buildAnoDeLancamento() {
        fProduct.anoDeLancamento = 2017;

    }

    @Override
    public void buildVersaoAtual() {
        fProduct.versaoAtual = 3.3;

    }

    @Override
    public void buildAutorOriginal() {
        fProduct.autorOriginal = "Google";

    }

    @Override
    public void buildLinguagemOficial() {
        fProduct.linguagemOficial = "Dart";

    }

}